public class Stats {
    private int m;
    private int n;
    private int t;

    public Stats(int m, int n, int t) {
        this.m = m;
        this.n = n;
        this.t = t;
    }

    public int getN() {
        return n;
    }

    public int getM() {
        return m;
    }

    public int getT() {
        return t;
    }
}
